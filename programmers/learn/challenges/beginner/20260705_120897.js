function solution(n) {
    let result = [];
    for(let i = 1; i <= n; i++) {
        if(n % i === 0) result.push(i);
    }
    return result;
}

// 1. 기존 풀이
// - for문은 의도가 명확하고 불필요한 배열 생성이 없다.

// 2. 다른 풀이
// - 가독성은 좋지만 중간 배열이 여러 번 생성되어 메모리 사용량과 실행 시간이 더 든다.
