// 3. 핵심 기준 ✨

// 3-1. 기본 원칙
// - “충분히 빠르고 읽기 쉬운 코드”를 우선한다.
//   코드 작성 시간보다 읽고 유지보수하는 비용이 더 크기 때문에 가독성이 기본값이다.
//   따라서 “조금 빠른 코드”보다 “바로 이해되는 코드”가 더 가치 있다.
// - 성능은 문제가 생겼을 때만 최적화한다.

// 3-2. 가독성을 선택하는 경우

// (1) 유지보수 코드
// (2) 성능 차이가 체감되지 않는 경우
// (3) 일반적인 알고리즘 / 코딩테스트
// - 가독성이 좋은 정석 풀이가 기본이며, 대부분 O(n), O(n log n)으로 해결 가능하다. (n ≤ 10,000 ~ 1,000,000)
// - 성능보다 명확한 로직과 버그 없는 정확성이 더 중요하다.
// - 가독성 평가 기준:
//   1) 문제 이해도: 문제를 정확히 이해했는가
//   2) 구현의 명확성: 로직을 깔끔하게 표현했는가

// 3-3. 성능을 선택하는 경우

// - 아래 조건에 해당할 때만 알고리즘을 개선한다
//   - 데이터가 수십만 ~ 수백만 이상일 때
//   - 반복이 중첩되어 O(n²) 구조로 시간 초과가 예상될 때
//   - 실제 병목이 측정된 경우 (profiling 기반)
//
// - “읽기 쉬운 정석 풀이”가 기본이며, 성능은 시간 초과가 발생할 때만 고려한다