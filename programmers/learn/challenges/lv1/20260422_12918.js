function solution(s) {
    return (s.length === 4 || s.length === 6) && /^\d+$/.test(s);
}

// 1. 정규식 정리
// - 1) /^[0-9]+$/.test(s)
//      정규식은 문자열을 그대로 검사하고, Number, isNaN 함수 등 다른 방식들은 내부적으로 변환을 거쳐 검사하므로 정규식이 더 정확히 예측 가능하다.
//
//      []      : 문자 집합 (괄호 안의 문자 중 하나와 매칭)
//      [0-9]   : 숫자 1개와 매칭 (0~9 중 하나)
//      [0-9]+  : 숫자가 1개 이상 연속으로 존재하면 매칭 (문자열 어디든 가능)
//      ^[0-9]+$: 문자열 전체가 숫자로만 이루어져 있어야 함
//      ^\d+$   : 위와 동일 (단, 일부 환경에서는 유니코드 숫자까지 포함될 수 있음)
//
// - 2) 길이 조건까지 정규식으로 처리 가능
//      return /^\d{4}$|^\d{6}$/.test(s);

// 2. isNaN 사용 시 문제점
// - 1) 공백(" "), 빈 문자열("")도 숫자로 변환되어 통과될 수 있다.
// - 2) "1e22" 같은 지수 표기법도 숫자로 처리된다.
// - 3) 즉 "숫자로 변환 가능" 여부만 검사하기 때문에 문자열 검증에는 부적합하며 위와 같은 이상한 케이스들을 제대로 걸러내지 못한다.

