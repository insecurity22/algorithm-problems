function solution(arr, k) {
    const kArr = [...new Set(arr)].slice(0, k);
    while(kArr.length < k) {
        kArr.push(-1);
    }
    return kArr;
}

// 1. while문을 사용하지 않는 방법
// - 부족한 개수만큼 -1로 채운 배열을 생성한 뒤 기존 배열에 추가한다.
// [...result, ...Array(k - result.length).fill(-1)]

// 2. 방법 차이

// 2-1) while
// - 추가 배열을 만들지 않아 동작이 직관적이며 메모리 사용이 조금 더 적다.

// 2-2) fill + 전개 연산자
// - 코드가 짧고, 부족한 만큼 채워 반환이라는 의도가 잘 드러나지만
// - 전개 연산자를 거치면서 새 배열을 한 번 더 생성한다.

// 2-3) 성능 비교
// - k = 100000 같이 매우 크다면, while + push가 메모리와 성능 면에서 유리하다.
// - Array(99999).fill(-1) 같은 임시 배열을 만들지 않기 때문이다.

// - 둘 다 시간복잡도는 O(k)이며, 제한이 작아서 사실 체감 차이는 거의 없지만,
// - 코드 리뷰를 한다면 2-1번을 선호할 것 같다. 이유는 불필요한 배열 생성이 없고, 초보자도 읽기 쉽고, 디버깅 하기 쉽기 때문이다.